    /**
     * 先去重，在使用二分查找
     * 
     * @param nums
     * @param target
     * @return
     */
    public boolean search(int[] nums, int target) {

        if (nums.length == 0) {
            return false;
        }
        if (nums.length == 1) {
            return target == nums[0];
        }

        int l = 0;
        int r = nums.length - 1;
        while (l <= r) {
            while (l < r && nums[l] == nums[l + 1]) {
                l++;
            }
            while (l < r && nums[r] == nums[r - 1]) {
                r--;
            }

            int mid = (l + r) >> 1;
            if (nums[mid] == target) {
                return true;
            } else if (nums[mid] < nums[r]) {
                // 右边有序
                if (nums[mid] < target && target <= nums[r]) {
                    l = mid + 1;
                } else {
                    r = mid - 1;
                }
            } else {
                // 左边有序
                if (nums[mid] > target && target >= nums[l]) {
                    r = mid - 1;
                } else {
                    l = mid + 1;
                }
            }
        }

        return false;
    }
